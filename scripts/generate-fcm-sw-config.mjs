import { readFileSync, writeFileSync, existsSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');

function loadEnv() {
  const env = {};
  for (const file of ['.env', '.env.local']) {
    const path = resolve(root, file);
    if (!existsSync(path)) continue;
    for (const line of readFileSync(path, 'utf8').split('\n')) {
      const match = line.match(/^([^#=]+)=(.*)$/);
      if (!match) continue;
      env[match[1].trim()] = match[2].trim().replace(/^["']|["']$/g, '');
    }
  }
  return env;
}

const env = loadEnv();

function envVar(key) {
  return process.env[key] ?? env[key] ?? '';
}

const REQUIRED_KEYS = [
  'NEXT_PUBLIC_FIREBASE_API_KEY',
  'NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN',
  'NEXT_PUBLIC_FIREBASE_PROJECT_ID',
  'NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET',
  'NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID',
  'NEXT_PUBLIC_FIREBASE_APP_ID',
];

const config = {
  apiKey: envVar('NEXT_PUBLIC_FIREBASE_API_KEY'),
  authDomain: envVar('NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN'),
  projectId: envVar('NEXT_PUBLIC_FIREBASE_PROJECT_ID'),
  storageBucket: envVar('NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET'),
  messagingSenderId: envVar('NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID'),
  appId: envVar('NEXT_PUBLIC_FIREBASE_APP_ID'),
};

const isProductionBuild =
  process.env.VERCEL === '1' ||
  process.env.CI === 'true' ||
  process.env.npm_lifecycle_event === 'build';

const missing = REQUIRED_KEYS.filter((key) => !envVar(key));
if (isProductionBuild && missing.length > 0) {
  console.error(
    '[FCM] Missing required Firebase env vars for service worker config:',
    missing.join(', '),
  );
  process.exit(1);
}

if (!config.apiKey || !config.projectId) {
  console.warn('[FCM] Firebase config incomplete — background push will not work in this build');
}

const output = `// Auto-generated by scripts/generate-fcm-sw-config.mjs — do not edit
self.__FIREBASE_CONFIG__ = ${JSON.stringify(config, null, 2)};
`;

writeFileSync(resolve(root, 'public/firebase-messaging-sw-config.js'), output);
console.log('[FCM] Generated public/firebase-messaging-sw-config.js (gitignored — do not commit)');
